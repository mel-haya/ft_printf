#include "libft.h"

void	*ft_memset(void *str, int c, size_t n)
{
	size_t	i;
	char	*pstr;

	pstr = (char*)str;
	i = 0;
	while (i < n)
	{
		pstr[i] = c;
		i++;
	}
	return (str);
}
