int	ft_isalpha(int c)
{
	if ((c >= 65 && c <= 90) || (c >= 97 && c <= 122))
		return (1);
	return (0);
}

int	ft_isalpha_percent(int c)
{
	if ((c >= 65 && c <= 90) || (c >= 97 && c <= 122) || c == '%')
		return (1);
	return (0);
}
